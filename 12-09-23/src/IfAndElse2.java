public class IfAndElse2 {

    public static void main(String[] args) {

        int nota = 7;

        if (nota >= 7)

            System.out.println("Aprovado");
        else if (nota >= 5 && nota < 7)

            System.out.println("Aluno de recuperacao");
        else

            System.out.println("Reprovado");
    }

}
